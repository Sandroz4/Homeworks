# Define the number of cycles you want to simulate
num_cycles = 4

cycle_count = 0

while cycle_count < num_cycles:
    print("Cycle", cycle_count + 1)
    
    # Morning Routine
    print("Wake up")
    print("Eat breakfast")
    
    # Work
    print("Work")
    
    # Evening Routine
    print("Eat dinner")
    print("Relax")
    print("Sleep")
    
    cycle_count += 1

print("Daily routine completed!")

